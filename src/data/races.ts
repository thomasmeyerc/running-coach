export function getRacesByDistance(distance: string): Race[] {
  return RACES.filter((r) => r.distance === distance);
}

export function getRacesByContinent(continent: string): Race[] {
  return RACES.filter((r) => r.continent === continent);
}

export function searchRaces(query: string): Race[] {
  const q = query.toLowerCase();
  return RACES.filter(
    (r) =>
      r.name.toLowerCase().includes(q) ||
      r.city.toLowerCase().includes(q) ||
      r.country.toLowerCase().includes(q) ||
      r.description.toLowerCase().includes(q) ||
      r.tags.some((t) => t.replace("_", " ").includes(q))
  );
}
